// ============================================
// So sÃ¡nh DEFAULT_FRAMES_JSON vá»›i localStorage
// Ä‘á»ƒ kiá»ƒm tra xem cÃ³ frames má»›i khÃ´ng

export function checkForUpdates() {
    const existingFrames = localStorage.getItem('photobooth_external_frames');
    
    if (!existingFrames || DEFAULT_FRAMES_JSON.length === 0) {
        return {
            hasUpdates: false,
            message: 'No comparison possible'
        };
    }
    
    const existing = JSON.parse(existingFrames);
    const existingUrls = new Set(existing.map(f => f.url));
    const defaultUrls = new Set(DEFAULT_FRAMES_JSON.map(f => f.url));
    
    // Check for new frames in DEFAULT_FRAMES_JSON
    const newFrames = DEFAULT_FRAMES_JSON.filter(f => !existingUrls.has(f.url));
    
    // Check for removed frames
    const removedFrames = existing.filter(f => !defaultUrls.has(f.url));
    
    return {
        hasUpdates: newFrames.length > 0 || removedFrames.length > 0,
        newFrames: newFrames.length,
        removedFrames: removedFrames.length,
        currentCount: existing.length,
        defaultCount: DEFAULT_FRAMES_JSON.length
    };
}
